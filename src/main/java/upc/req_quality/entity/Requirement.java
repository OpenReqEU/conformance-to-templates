package upc.req_quality.entity;

public class Requirement {

    private String id;
    private String text;

    public Requirement() {
    }

    public Requirement(String id, String text) {
        this.id = id;
        this.text = text;
    }

    public String getId() {
        return id;
    }

    public String getText() {
        return text;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setText(String text) {
        this.text = text;
    }
}
